/** Para este primer ejercicio tendréis que realizar lo siguiente:

 * 1 - Crea un proyecto de Java desde 0
 * 2 - Dentro del proyecto tenéis que crear un paquete. En el paquete tendréis que crear una clase.
 * 3 - Dentro de la clase tenéis que crear el método main e imprimir todos los datos que se han visto en las sesiones.

 Recordatorio: Los tipos de datos más comunes son int, long, double, boolean, String.*/
public class Ejercicio1 {

    public static void main(String[] args) {

        // 1. Numericos.

        // 1.1 Enteros.

        byte valor1 = 7;
        System.out.println(valor1);
        short valor2 = 50;
        System.out.println(valor2);
        int valor3 = 60;
        System.out.println(valor3);
        long valor4 = 200;
        System.out.println(valor4);


        // 1.2 Decimales.

        float valor5 = 3.5f;
        System.out.println(valor5);
        double valor6 = 14.6d;
        System.out.println(valor6);

        // 2. Booleanos.

        boolean valor7 = true;
        System.out.println(valor7);
        boolean valor8 = false;
        System.out.println(valor8);

        // 3. Texto.

        char valor9 = 'B';
        System.out.println(valor9);
        String valor10 = "Mensaje de prueba";
        System.out.println(valor10);

    }

}

